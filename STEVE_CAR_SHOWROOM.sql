-- 7. What is the total revenue generated by the sales of hatchback cars?SELECT style, SUM(cost_$) 'Revenue' FROM salesLEFT JOIN carsON sales.car_id = cars.car_idWHERE style = 'Hatchback'GROUP BY style-- 8. What is the total revenue generated by the sales of SUV cars in the year 2022?SELECT style, SUM(cost_$) 'Revenue' FROM salesLEFT JOIN carsON sales.car_id = cars.car_idWHERE style = 'SUV' AND YEAR(purchase_date) = 2022GROUP BY style

-- 9. What is the name and city of the salesperson who sold the most number of 
-- cars in the year 2023?

SELECT TOP 1 name, city, COUNT(sale_id) 'Sales_count' FROM sales
LEFT JOIN salespersons
ON sales.salesman_id = salespersons.salesman_id
WHERE YEAR(purchase_date) = 2023
GROUP BY name, city
ORDER BY Sales_count DESC


